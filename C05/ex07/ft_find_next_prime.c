int	ft_find_next_prime(int nb)
{
	int	i;
	int	j;

	i = nb;
	if (nb <= 0)
		return (0);
	while (i >= nb && (i * i <= 2147483647))
	{
		j = 2;
		while ((i % j) != 0 && (j * j <= i))
			j++;
		if ((j * j) > i)
			return (i);
		i++;
	}
	return (0);
}
